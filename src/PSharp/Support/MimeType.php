	/**
	 * Try detect mime-type from the given filename.
	 *
	 * @static
	 * @param string $filename
	 * @return string
	 */
	public static function from($filename)
	{
		$extension = pathinfo($filename, PATHINFO_EXTENSION);
		//
		return self::getMimeTypeFromExtension($extension);
	}

	/**
	 * Try detect mime-type from the given extension.
	 *
	 * @static
	 * @param string|null $extension
	 * @return string
	 */
	public static function get($extension = null)
	{
		return $extension ? self::getMimeTypeFromExtension($extension) : self::$mimes;
	}

	/**
	 * Try detect mime-type description from the given extension.
	 *
	 * @static
	 * @param string|null $extension
	 * @return string
	 */
	public static function explain($extension = null)
	{
		return $extension ? self::getMimeTypeFromExtension($extension) : self::$mimeDescriptions;
	}

	/**
	 * Return the extension and description from the given mime-type.
	 *
	 * @static
	 * @param string|null $mimeType
	 * @return string
	 */
	public static function search($mimeType)
	{
		return array_search($mimeType, self::$mimes) ?: null;
	}

	/**
	 * Return the mime-type from the given extension.
	 *
	 * @static
	 * @param string|null $extension
	 * @return string
	 */
	public static function getMimeTypeFromExtension($extension)
	{
		return self::$mimes[$extension] ?? 'application/octet-stream';
	}
